    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String truyvan = txtTimKiem.getText();
        indexcbSelected = cbLuaChon.getSelectedIndex();
        if(indexcbSelected == 0)
        {
            listDocGia = new DAO_ChiTietPhieuMuon().TimDocGia(truyvan);
            model.setNumRows(0);
            int i = 1;
            for(DocGia obj:listDocGia)
            {
                model.addRow(new Object[]{
                i++, obj.getMadocgia(),obj.getTendocgia(),obj.getNgaysinh(),obj.getGioitinh(),obj.getDiachi(),obj.getSdt(),obj.getNhom()
                });
            }
        }else if(indexcbSelected == 1)
        {
            listSach = new DAO_ChiTietPhieuMuon().TimSach(truyvan);
            model.setNumRows(0);
            int i = 1;
            for(Sach obj:listSach)
            {
                model.addRow(new Object[]{
                i++, obj.getMasach(),obj.getTensach(),obj.getTentacgia(),obj.getNoixuatban(),obj.getNamxuatban(),obj.getLoai()
                });
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

