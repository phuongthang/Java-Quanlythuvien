    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        String sophieumuon = txtMaPhieuMuon.getText();
        PhieuMuon phieumuon = new PhieuMuon();
        phieumuon.setMadocgia(txtMaDocGia.getText());
        phieumuon.setMaphieumuon(txtMaPhieuMuon.getText());
        try {
            phieumuon.setNgaytra(new SimpleDateFormat("dd/MM/yyyy").parse(txtNgayTra.getText()));
        } catch (ParseException ex) {
        }
        list.add(phieumuon);
        if(new DAO_ChiTietPhieuMuon().updatePM(phieumuon))
        {
            getData(sophieumuon);
            clearText();
            btnSua.setEnabled(false);
            btnXoa.setEnabled(false);
            
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int result = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn xóa không","Question",JOptionPane.YES_NO_OPTION);
        if(result==JOptionPane.YES_OPTION)
        {
            String masach = txtMaSach.getText();
            String sophieumuon = txtMaPhieuMuon.getText();
            if(new DAO_ChiTietPhieuMuon().deleteCTPhieuMuon(masach, sophieumuon))
            {
                
                JOptionPane.showMessageDialog(rootPane, "Xóa thành công !","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                getData(sophieumuon); 
                clearText();
                btnSua.setEnabled(false);
                btnXoa.setEnabled(false);
            }
        }
        
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThoatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThoatActionPerformed
        if(checkout == 0)
        {
            this.setVisible(false);
            fHeThong hethong = new fHeThong();
            hethong.setLocationRelativeTo(null);
            hethong.setVisible(true);
        }
        else if(checkout == 1)
        {
            int result = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn thoát không. Mọi thông tin sẽ không được lưu lại","Question",JOptionPane.YES_NO_OPTION);
            if(result==JOptionPane.YES_OPTION)
            {
                String sophieumuon = txtMaPhieuMuon.getText();
                if(new DAO_ChiTietPhieuMuon().deletePM(sophieumuon))
                {
                    this.setVisible(false);
                    fHeThong hethong = new fHeThong();
                    hethong.setLocationRelativeTo(null);
                    hethong.setVisible(true);
                }
            }
        }
        else if(checkout == 2)
        {
            int result = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn thoát không. Mọi thông tin sẽ không được lưu lại","Question",JOptionPane.YES_NO_OPTION);
            if(result==JOptionPane.YES_OPTION)
            {
                String sophieumuon = txtMaPhieuMuon.getText();
                if(new DAO_ChiTietPhieuMuon().deleteCTPM(sophieumuon))
                {
                    if(new DAO_ChiTietPhieuMuon().deletePM(sophieumuon))
                    {
                        this.setVisible(false);
                        fHeThong hethong = new fHeThong();
                        hethong.setLocationRelativeTo(null);
                        hethong.setVisible(true);
                    }
                }
                else
                {
                    this.setVisible(false);
                    fHeThong hethong = new fHeThong();
                    hethong.setLocationRelativeTo(null);
                    hethong.setVisible(true);
                }
            }
        }
        else if(checkout == 3)
        {
            this.setVisible(false);
            fHeThong hethong = new fHeThong();
            hethong.setLocationRelativeTo(null);
            hethong.setVisible(true);
        }
    }//GEN-LAST:event_btnThoatActionPerformed

