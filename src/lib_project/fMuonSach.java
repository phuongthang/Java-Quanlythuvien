    public final void getData(String sophieumuon)
    {
        model1.setNumRows(0);
        list = new DAO_ChiTietPhieuMuon().getListPhieuMuon(sophieumuon);
        int i = 1;
        for(PhieuMuon obj:list)
        {
            model1.addRow(new Object[]{
            i++, obj.getMaphieumuon(),obj.getMadocgia(),obj.getTendocgia(),obj.getNgaymuon(),obj.getNgaytra(),obj.getMasach(),obj.getTensach()
            });
        }
    }
