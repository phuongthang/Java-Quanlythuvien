    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        Sach sach = new Sach();
        sach.setMasach(txtMaSach.getText());
        sach.setTensach(txtTenSach.getText());
        sach.setTentacgia(txtTenTacGia.getText());
        sach.setNoixuatban(txtNoiXuatBan.getText());
        sach.setNamxuatban(cbNamXuatBan.getItemAt(cbNamXuatBan.getSelectedIndex()));
        sach.setLoai(cbLoai.getItemAt(cbLoai.getSelectedIndex()));
        list.add(sach);
        if(new DAO_Sach().addSach(sach))
        {
            JOptionPane.showMessageDialog(rootPane, "Thành công");
            clearText();
            getData();
            
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Đã tồn tại mã nhà cung cấp");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void tableSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSachMouseClicked
        int id = tableSach.getSelectedRow();
        if(id>=0)
        {
            txtMaSach.setText((String) model.getValueAt(id, 1));
            txtTenSach.setText((String) model.getValueAt(id, 2));
            txtTenTacGia.setText((String) model.getValueAt(id, 3));
            txtNoiXuatBan.setText((String) model.getValueAt(id, 4));
            cbNamXuatBan.setSelectedItem((String) model.getValueAt(id, 5));
            cbLoai.setSelectedItem((String) model.getValueAt(id, 6));
            txtMaSach.setEditable(false);
            btnThem.setEnabled(false);
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }//GEN-LAST:event_tableSachMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        Sach sach = new Sach();
        sach.setMasach(txtMaSach.getText());
        sach.setTensach(txtTenSach.getText());
        sach.setTentacgia(txtTenTacGia.getText());
        sach.setNoixuatban(txtNoiXuatBan.getText());
        sach.setNamxuatban(cbNamXuatBan.getItemAt(cbNamXuatBan.getSelectedIndex()));
        sach.setLoai(cbLoai.getItemAt(cbLoai.getSelectedIndex()));
        list.add(sach);
        if(new DAO_Sach().updateSach(sach))
        {
            JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
            clearText();
            getData();
            btnThem.setEnabled(true);
            btnSua.setEnabled(false);
            btnXoa.setEnabled(false);
            
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int result = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc muốn xóa không","Question",JOptionPane.YES_NO_OPTION);
        if(result==JOptionPane.YES_OPTION)
        {
            String masach = txtMaSach.getText();
            if(new DAO_Sach().deleteSach(masach))
            {
                JOptionPane.showMessageDialog(rootPane, "Xóa thành công !","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                clearText();
                getData();
                btnThem.setEnabled(true);
                btnSua.setEnabled(false);
                btnXoa.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String truyvan = txtTimKiem.getText();
        list = new DAO_Sach().findSach(truyvan);
        model.setNumRows(0);
        int i = 1;
        for(Sach obj:list)
        {
            model.addRow(new Object[]{
            i++, obj.getMasach(),obj.getTensach(),obj.getTentacgia(),obj.getNoixuatban(),obj.getNamxuatban(),obj.getLoai()
            });
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

