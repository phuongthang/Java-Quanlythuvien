    public final void getData()
    {
        model.setNumRows(0);
        list = new DAO_Sach().getListSach();
        int i = 1;
        for(Sach obj:list)
        {
            model.addRow(new Object[]{
            i++, obj.getMasach(),obj.getTensach(),obj.getTentacgia(),obj.getNoixuatban(),obj.getNamxuatban(),obj.getLoai()
            });
        }
        txtMaSach.setEnabled(true);
        long now = Instant.now().toEpochMilli();
        txtMaSach.setText("S"+String.valueOf(now));
    }
