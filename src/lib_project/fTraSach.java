    public final void getData(String ten)
    {
        model.setNumRows(0);
        list = new DAO_PhieuTra().getListDocGia(ten);
        int i = 1;
        for(PhieuTra obj:list)
        {
            model.addRow(new Object[]{
            i++, obj.getMadocgia(),obj.getTendocgia(),obj.getNgaysinh(),obj.getDiachi(),obj.getSdt(),obj.getNhom(),obj.getSophieumuon(),obj.getNgaymuon()
            });
        }
    }
    public final void getDataPhieuMuon(String sophieumuon)
    {
        model1.setNumRows(0);
        listphieumuon = new DAO_ChiTietPhieuMuon().getListPhieuMuon(sophieumuon);
        int i = 1;
        for(PhieuMuon obj:listphieumuon)
        {
            model1.addRow(new Object[]{
            i++, obj.getMaphieumuon(),obj.getMadocgia(),obj.getTendocgia(),obj.getNgaymuon(),obj.getNgaytra(),obj.getMasach(),obj.getTensach()
            });
        }
    }
