    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed

            ChiTietPhieuTra ctphieutra = new ChiTietPhieuTra();
            ctphieutra.setSophieutra(txtMaPhieuTra.getText());
            ctphieutra.setMabandoc((String) model1.getValueAt(0, 2));
            ctphieutra.setSophieumuon((String) model1.getValueAt(0, 1));
            try {
            ctphieutra.setNgaytra(new SimpleDateFormat("dd/MM/yyyy").parse(txtNgayTra.getText()));
            } catch (ParseException ex) {
            }
            ArrayList<ChiTietPhieuTra> listt = new ArrayList<>();
            if(new DAO_PhieuTra().addPhieuTra(ctphieutra))
            {
                for(int i =0;i< model1.getRowCount();i++)
                {
                    ChiTietPhieuTra ctpt = new ChiTietPhieuTra();
                    ctpt.setSophieutra(txtMaPhieuTra.getText());
                    ctpt.setMasach((String) model1.getValueAt(i, 6));
                    
                    try {
                        if((boolean)model1.getValueAt(i,8)==true)
                        {
                            ctpt.setTinhtrang("Nguyên Vẹn");
                        }
                    } catch (Exception e) {
                    }
                    try {
                        if((boolean)model1.getValueAt(i,9)==true)
                        {
                            ctpt.setTinhtrang("Không Nguyên Vẹn");
                        }
                    } catch (Exception e) {
                    }
                    try {
                        if((boolean)model1.getValueAt(i,10)==true)
                        {
                            ctpt.setTinhtrang("Bị Mất");
                        }
                    } catch (Exception e) {
                    }
                    listt.add(ctpt);
                }
            
                for(ChiTietPhieuTra phieu:listt)
                {
                    new DAO_PhieuTra().addCTPhieuTra(phieu);
                }
                JOptionPane.showMessageDialog(rootPane, "Hoàn thành !","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                btnLuu.setEnabled(false);
                
            }
            
                
        
    }//GEN-LAST:event_btnLuuActionPerformed

